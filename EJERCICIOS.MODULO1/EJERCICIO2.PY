# Ejercicio 2: Gestión de Clientes
# Enunciado:
# Crea un sistema de gestión de clientes para una empresa. El sistema debe tener las siguientes funcionalidades:
# 1. **Registrar un cliente**: Ingresar el ID del cliente, nombre y teléfono.
# 2. **Mostrar los clientes registrados**: Mostrar el ID, nombre y teléfono de todos los clientes.
# 3. **Actualizar el teléfono de un cliente**: Permitir modificar el teléfono de un cliente dado su ID.
# 4. **Eliminar un cliente**: Eliminar un cliente del sistema dado su ID.
# 5. **Salir**: Guardar los cambios realizados y salir del programa.
# Los datos deben ser guardados en un archivo de texto (`clientes.txt`), y el sistema debe permitir cargar los datos desde el archivo al iniciar y guardarlos al finalizar la ejecución.
